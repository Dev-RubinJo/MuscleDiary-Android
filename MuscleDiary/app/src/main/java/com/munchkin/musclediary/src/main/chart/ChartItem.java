package com.munchkin.musclediary.src.main.chart;

public class ChartItem {
    private String level;
    private String date;

    public void setLevel(String level) {
        this.level = level;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getLevel() {
        return level;
    }

    public String getDate() {
        return date;
    }
}
